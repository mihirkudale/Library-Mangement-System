    public void ViewLibrarianData() {
        ids = new ArrayList<>();
        String data[][] = null;
        String column[] = null;
        DefaultTableModel model = new DefaultTableModel();
        try {
            Connection con = DB.getConnection();
            PreparedStatement ps = con.prepareStatement("select * from librarian", ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = ps.executeQuery();

            ResultSetMetaData rsmd = rs.getMetaData();
            int cols = rsmd.getColumnCount();
            column = new String[cols];
            for (int i = 1; i <= cols; i++) {
                column[i - 1] = rsmd.getColumnName(i);
            }

            rs.last();
            int rows = rs.getRow();
            rs.beforeFirst();

            data = new String[rows][cols];
            int count = 0;
            while (rs.next()) {
                for (int i = 1; i <= cols; i++) {
                    data[count][i - 1] = rs.getString(i);
                }
                count++;
            }
            con.close();
        } catch (Exception e) {
            System.out.println(e);
        }
        for (int j = 0; j < column.length; j++) {
            if (!column[j].equals("photo")) {
                model.addColumn(column[j]);
            }
        }

        for (int i = 0; i < data.length; i++) {
            model.addRow(data[i]);
            System.out.println("data[i] : " + data[i]);
            
            for (int j = 0; j < data[i].length; j++) {
                String str = data[i][j];
                System.out.println("str : " + str);
                if (j == 0) {
                    ids.add(str);
                }
            }
        }
        jTable1.setModel(model);
        for (int i = 0; i < ids.size(); i++) {
            String getID = ids.get(i);
            jComboBox1.addItem(getID);
        }
    }

