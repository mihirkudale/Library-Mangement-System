private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
    // TODO add your handling code here:

//    DaoConnection dao = new DaoConnection();
    if (("".equals(tfID.getText())) || ("".equals(tfStudentName.getText())) || ("".equals(tfBookCellNo.getText())) || ("".equals(tfStuentContact.getText()))) {
        JOptionPane.showMessageDialog(null, "Please Enter All The Fields !!!");
    } else {

//        int quantity = 0;
        String bookcallno = tfBookCellNo.getText();
        String studentid = tfID.getText();
        String studentname = tfStudentName.getText();
        String studentcontact = tfStuentContact.getText();

        if (IssueBookDao.checkBook(bookcallno)) {
            int checkQuantity = IssueBookDao.checkQuantity(studentid);
//            System.out.println("checkQuantity : "+checkQuantity);
            if (checkQuantity < 2) {

                int i = IssueBookDao.save(bookcallno, studentid, studentname, studentcontact);
                if (checkQuantity == 0) {
//                    System.out.println("checkQuantity 0 : "+checkQuantity);
                    IssueBookDao.saveQuantity(studentid, (checkQuantity + 1));
                }else{
//                    System.out.println("checkQuantity 1 : "+checkQuantity);
                    IssueBookDao.updateQuantity(studentid, (checkQuantity + 1));
                }
                if (i > 0) {
                    JOptionPane.showMessageDialog(this, "Book issued successfully!");
                    tfBookCellNo.setText("");
                    tfID.setText("");
                    tfStudentName.setText("");
                    tfStuentContact.setText("");
                    cbBookName.setSelectedItem("");
                } else {
                    JOptionPane.showMessageDialog(this, "Sorry, unable to issue!");
                }//end of save if-else
            } else {
                JOptionPane.showMessageDialog(this, "Sorry, You can't issue book!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Sorry, Callno doesn't exist!");
        }//end of checkbook if-else

    }
//        }
//    }
}//GEN-LAST:event_btnRegisterActionPerformed

