    public void comboFilter(String enteredText) {
        List<String> filterArray = new ArrayList<String>();
        String str1 = "";
        try {
            Connection con = DB.getConnection();
            String str = "SELECT name FROM books WHERE name LIKE '%" + enteredText + "%'";

//     Class.forName("sun.jdbc.odbc.JdbcOdbcDriver");
//      con=DriverManager.getConnection("jdbc:odbc:ds1", "sa" , "creative");
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery(str);
            while (rs.next()) {

                str1 = rs.getString("name");
                filterArray.add(str1);

            }
        } catch (Exception ex) {
            ex.printStackTrace();
            System.out.println("error" + ex);
        }

        if (filterArray.size() > 0) {
            cbBookName.setModel(new DefaultComboBoxModel(filterArray.toArray()));
            cbBookName.setSelectedItem(enteredText);
            cbBookName.showPopup();
        } else {
            cbBookName.hidePopup();
        }
    }

